async function seedDatabase() {
  console.log("🚀 Seeding Firestore commissions collection...");
  let successCount = 0;
  let failureCount = 0;

  const batchSize = 400;
  for (let i = 0; i < parsedCommissions.length; i += batchSize) {
    const chunk = parsedCommissions.slice(i, i + batchSize);
    const batch = db.batch();

    chunk.forEach(comm => {
      const docId = `${comm.poppo_id}_${comm.month}`;
      const docRef = db.collection('commissions').doc(docId);
      batch.set(docRef, comm);
    });

    try {
      await batch.commit();
      successCount += chunk.length;
      console.log(`✅ Uploaded batch: ${successCount} / ${parsedCommissions.length} records written.`);
    } catch (err) {
      console.error(`❌ Batch write failed:`, err);
      failureCount += chunk.length;
    }
  }

  // Auto-sync new hosts that might not have been registered yet
  try {
    console.log("🔍 Running roster validation sync...");
    const hostSnap = await db.collection('hosts').get();
    const existingIds = new Set(hostSnap.docs.map(doc => doc.id));
    const newHostsToRegister = [];

    parsedCommissions.forEach(c => {
      if (!existingIds.has(c.poppo_id) && c.poppo_id !== 'undefined' && c.poppo_id) {
        newHostsToRegister.push({
          id: c.poppo_id,
          name: c.poppo_name,
          nickname: c.poppo_name,
          position: 'Talent',
          role: 'Talent',
          team: 'Unassigned',
          manager: 'Nine Management',
          anchor_type: 'Nine Agency',
          base_salary_category: 'N/A',
          status: 'Active',
          level: c.level || 1,
          tier: 'X',
          created_at: new Date().toISOString(),
          updated_at: new Date().toISOString(),
          password: '1212',
          is_temp_password: true
        });
        existingIds.add(c.poppo_id); // Prevent duplicate additions in loop
      }
    });

    if (newHostsToRegister.length > 0) {
      console.log(`👤 Auto-registering ${newHostsToRegister.length} new hosts found in financial sheets...`);
      for (let i = 0; i < newHostsToRegister.length; i += batchSize) {
        const chunk = newHostsToRegister.slice(i, i + batchSize);
        const batch = db.batch();
        chunk.forEach(host => {
          batch.set(db.collection('hosts').doc(host.id), host);
        });
        await batch.commit();
      }
      console.log(`✅ Roster auto-sync complete.`);
    }
  } catch (syncErr) {
    console.warn("⚠️ Roster auto-sync skipped/warned: ", syncErr.message);
  }

  console.log(`\n🎉 SEED COMPLETE:`);
  console.log(`- Successful uploads: ${successCount}`);
  console.log(`- Failed uploads: ${failureCount}`);
  process.exit(0);
}

